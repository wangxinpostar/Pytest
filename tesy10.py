import turtle as t

t.setup(600,600)
t.bgcolor('white')

t.shape('turtle')
t.pensize(1)
t.pencolor('red')
t.speed(0)

for i in range(7):
    t.fillcolor('red')
    t.begin_fill()
    t.fd(150)
    t.left(120)
    t.fd(150)
    t.left(60)
    t.fd(150)
    t.left(60)
    t.fd(150)
    t.left(120)
    t.fd(300)
    t.left(120)
    t.fd(150)
    t.end_fill()

    t.fillcolor('green')
    t.begin_fill()
    t.fd(150)
    t.left(120)
    t.fd(150)
    t.left(120)
    t.fd(150)
    t.end_fill()
    t.up()
    t.goto(0,0)
    t.down()
    t.right(60)
t.ht()

t.done()
