import turtle as t

t.setup(0.7,0.7)
t.bgcolor('white')
t.hideturtle()
t.shape('turtle')
t.pensize(1)
t.pencolor('red')
t.speed(6)

t.fillcolor('yellow')
t.begin_fill()
t.forward(150)
t.left(120)
t.forward(300)
t.left(120)
t.forward(300)
t.left(120)
t.forward(150)
t.end_fill()

t.fillcolor('green')
t.begin_fill()
t.up()
t.goto(-150,-150)
t.down()
t.forward(300)
t.seth(90)
t.circle(150,180)
t.left(90)
t.end_fill()

t.ht()

t.done()
